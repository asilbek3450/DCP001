# Пример использования в Python для функций, аналогичных Haskell Data.Char

# Проверка на букву
is_letter_result = 'A'.isalpha()

# Перевод в верхний/нижний регистр
upper_case_result = 'a'.upper()
lower_case_result = 'B'.lower()

# Проверка на число
is_digit_result = '5'.isdigit()

print(f'Is Letter: {is_letter_result}')
print(f'Upper Case: {upper_case_result}')
print(f'Lower Case: {lower_case_result}')
print(f'Is Digit: {is_digit_result}')
