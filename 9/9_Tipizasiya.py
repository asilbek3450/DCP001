# Пример неявной типизации в Python

# Переменная 'a' автоматически становится целым числом (int)
a = 5
print("Переменная 'a':", a, ", Тип:", type(a))

# Переменная 'b' автоматически становится строкой (str)
b = "Привет, мир!"
print("Переменная 'b':", b, ", Тип:", type(b))

# Переменная 'c' автоматически становится числом с плавающей точкой (float)
c = 3.14
print("Переменная 'c':", c, ", Тип:", type(c))
