import pandas as pd

# Создадим DataFrame с данными о клиентах
клиенты = pd.DataFrame({
    'имя': ['Анна', 'Иван'],
    'дата_рождения': ['90-1-15', '85-10-20']
})

# Преобразуем формат даты в ГГГГ-ММ-ДД
клиенты['преобразованная_дата'] = pd.to_datetime(клиенты['дата_рождения']).dt.strftime('%Y-%m-%d')

# Вывод результатов
print(клиенты[['имя', 'преобразованная_дата']])
