import pandas as pd

# Создадим DataFrame с данными о клиентах и сумме их покупок
клиенты = pd.DataFrame({
    'имя': ['Анна', 'Иван', 'Мария', 'Петр'],
    'сумма_покупок': [1200, 800, 1500, 900]
})

# Отсортируем записи по убыванию суммы покупок
отсортированные_клиенты = клиенты.sort_values(by='сумма_покупок', ascending=False)

# Вывод результатов
print(отсортированные_клиенты)
