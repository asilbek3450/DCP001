import pandas as pd

# Создадим DataFrame с данными о продажах
продажи = pd.DataFrame({
    'дата': ['2023-01-01', '2023-01-15', '2023-02-01', '2023-02-15'],
    'сумма': [1200, 800, 1500, 900]
})

# Преобразуем столбец 'дата' в формат даты для удобства работы
продажи['дата'] = pd.to_datetime(продажи['дата'])

# Группировка данных по месяцам и вычисление общей суммы продаж для каждого месяца
группировка_по_месяцам = продажи.groupby(продажи['дата'].dt.to_period("M")).sum()

# Вывод результатов
print(группировка_по_месяцам)
