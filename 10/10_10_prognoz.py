import pandas as pd

# Создадим DataFrame с данными о продажах
продажи = pd.DataFrame({
    'дата': pd.date_range(start='2023-01-01', periods=5, freq='M'),
    'объем_продаж': [100, 120, 150, 130, 160]
})

# Добавим столбец с предыдущим объемом продаж
продажи['предыдущий_объем_продаж'] = продажи['объем_продаж'].shift()

# Вывод результатов
print(продажи[['дата', 'предыдущий_объем_продаж', 'объем_продаж']])
