import pandas as pd

# Предположим, у вас есть DataFrame с данными о транзакциях
транзакции = pd.DataFrame({
    'идентификатор': [1, 2, 3, 4],
    'сумма': [1200, 800, 1500, 900]
})

# Фильтрация данных: отберем транзакции с суммой больше 1000 долларов
отфильтрованные_транзакции = транзакции[транзакции['сумма'] > 1000]

# Вывод результатов
print(отфильтрованные_транзакции)
